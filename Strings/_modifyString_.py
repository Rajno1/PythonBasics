# Python has a set of built-in methods that you can use on strings

# to make all your string in upper case ,we can use 'upper()'
a="welcome"
print(a.upper())

# 'lower()' to get in lower case
b="HOW ARE YOU ?"
print(b.lower())

# we can remove white spaces before and after string
c=" where are you  "
print(c.strip())

# we use replace() to replace character
d="Hello"
print(d.replace("e","o"))   # Here we are replacing 'e' with 'o'

# The split() method returns a list where the text between the specified separator becomes the list items.
e="Hello, World"
print(e.split(",")) # ',' is the separator

